def _write_spawners(parent: ET.Element, spawners: list[WoolSpawner]) -> None:
    block = ET.SubElement(parent, 'spawners')
    for s in spawners:
        attrs: dict = {
            'spawn-region': s.spawn_region,
            'player-region': s.player_region,
        }
        if s.delay:
            attrs['delay'] = s.delay
        if s.max_entities is not None:
            attrs['max-entities'] = str(s.max_entities)
        elem = ET.SubElement(block, 'spawner', **attrs)
        for item in s.items:
            item_attrs: dict = {'material': item.material}
            if item.damage:
                item_attrs['damage'] = str(item.damage)
            if item.amount != 1:
                item_attrs['amount'] = str(item.amount)
            ET.SubElement(elem, 'item', **item_attrs)


def _write_renewables(parent: ET.Element, renewables: list[Renewable]) -> None:
    block = ET.SubElement(parent, 'renewables')
    for r in renewables:
        attrs: dict = {'region': r.region_id}
        if r.rate != 1.0:
            attrs['rate'] = str(r.rate)
        if r.renew_filter:
            attrs['renew-filter'] = r.renew_filter
        if r.replace_filter:
            attrs['replace-filter'] = r.replace_filter
        if r.grow:
            attrs['grow'] = 'true'
        ET.SubElement(block, 'renewable', **attrs)


def _write_block_drops(parent: ET.Element, rules: list[BlockDropRule]) -> None:
    block = ET.SubElement(parent, 'block-drops')
    for rule in rules:
        attrs: dict = {}
        if rule.region_id:
            attrs['region'] = rule.region_id
        if rule.filter_id:
            attrs['filter'] = rule.filter_id
        if rule.wrong_tool:
            attrs['wrong-tool'] = 'true'
        rule_elem = ET.SubElement(block, 'rule', **attrs)
        if rule.replacement:
            ET.SubElement(rule_elem, 'replacement').text = rule.replacement
        if rule.items:
            drops = ET.SubElement(rule_elem, 'drops')
            for item in rule.items:
                item_attrs: dict = {'material': item.material}
                if item.damage:
                    item_attrs['damage'] = str(item.damage)
                if item.amount != 1:
                    item_attrs['amount'] = str(item.amount)
                if item.chance != 1.0:
                    item_attrs['chance'] = str(item.chance)
                ET.SubElement(drops, 'item', **item_attrs)


# ---------------------------------------------------------------------------
# Indentation helper (Python 3.8 compatible fallback)
# ---------------------------------------------------------------------------

def _indent(elem: ET.Element, level: int = 0) -> None:
    indent = '\n' + '    ' * level
    if len(elem):
        if not elem.text or not elem.text.strip():
            elem.text = indent + '    '
        if not elem.tail or not elem.tail.strip():
            elem.tail = indent
        for child in elem:
            _indent(child, level + 1)
        if not child.tail or not child.tail.strip():
            child.tail = indent
    else:
        if level and (not elem.tail or not elem.tail.strip()):
            elem.tail = indent
    if not level:
        elem.tail = '\n'
